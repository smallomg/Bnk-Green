package Inheritance;

public class SignUp {

	String id; // 아이디
	String password; // 비밀번호

	SignUp() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
