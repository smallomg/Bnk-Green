package ex01;

public class Calulator {
	
	/*멤버변수가 없다*/
	/*
	 * int Num1; int Num2;
	 */

	int add(int Num1, int Num2) {
		return Num1 + Num2;
	}

	int sub(int Num1, int Num2) {
		return Num1 - Num2;
	}

	int mul(int Num1, int Num2) {
		return Num1 * Num2;
	}

	int div(int Num1, int Num2) {
		return Num1 / Num2;
	}
}
